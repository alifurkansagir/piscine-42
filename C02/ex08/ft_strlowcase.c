char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (i[str])
	{
		if (i[str] >= 'A' && i[str] <= 'Z')
		{
			i[str] += 32;
		}
		i++;
	}
	return (str);
}
