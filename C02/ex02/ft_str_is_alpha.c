int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (i[str] == '\0')
	{
		return (1);
	}
	while (i[str])
	{
		if ((i[str] >= 'A' && str [i] <= 'Z')
			|| (str [i] >= 'a' && str [i] <= 'z'))
			i++;
		else
			return (0);
	}
	return (1);
}
