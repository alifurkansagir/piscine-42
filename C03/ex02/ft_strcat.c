char	*ft_strcat(char *dest, char *src)
{
	int	d;
	int	i;

	d = 0;
	while (dest[d])
		d++;
	i = 0;
	while (src[i])
	{
		dest[d + i] = src[i];
		i++;
	}
	dest[d + i] = '\0';
	return (dest);
}